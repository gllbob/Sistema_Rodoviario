    public void ListagemVeiculo() throws SQLException {
        VeiculoDAO veiculodao = new VeiculoDAO();
        List<Veiculo> listaveiculos = veiculodao.listarTodosVeiculos();

        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Placa");
        model.addColumn("Motorista");
        model.addColumn("Modelo");
        model.addColumn("Ano do Modelo");
        model.addColumn("Quantidade de Poltronas");
        model.addColumn("NÃºmero");

        for (Veiculo veiculo : listaveiculos) {
            model.addRow(new Object[]{
                veiculo.getPlaca(),
                veiculo.getMotorista(),
                veiculo.getModelo(),
                veiculo.getAnomodelo(),
                veiculo.getQtdpoltronas(),
                veiculo.getNumero()
            });
        }

        tlistaveiculo.setModel(model);
    }
