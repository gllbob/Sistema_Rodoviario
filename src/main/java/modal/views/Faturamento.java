    public void ListagemPassagem() throws SQLException {
        try {
            // Other code ...
            
            Double totfaturamento = 0.0;
            
            PassagemDAO passagemdao = new PassagemDAO();
            SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");

            // Assuming tfdtcinicio and tfdtcfinal are JTextFields
            String dtcInicioText = tfdtcinicio.getText();
            String dtcFinalText = tfdtcfinal.getText();

            Date dtcInicio = dateFormat.parse(dtcInicioText);
            Date dtcFinal = dateFormat.parse(dtcFinalText);

            List<Passagem> listapassagems = passagemdao.faturamento(dtcInicio, dtcFinal);
            
            DefaultTableModel model = new DefaultTableModel();
            model.addColumn("COD Passagem");
            model.addColumn("Cidade Origem");
            model.addColumn("Cidade Destino");
            model.addColumn("Placa Veiculo");
            model.addColumn("Poltrona");
            model.addColumn("Data Saida");
            model.addColumn("Hora Saida");
            model.addColumn("Valor Passagem");

           for (Passagem passagem : listapassagems) {
                model.addRow(new Object[]{
                    passagem.getIdpassagem(),
                    passagem.getCidadeorigem() != null ? passagem.getCidadeorigem().getNomeCidade() : "",
                    passagem.getCidadedestino() != null ? passagem.getCidadedestino().getNomeCidade() : "",
                    passagem.getVeiculo() != null ? passagem.getVeiculo().getPlaca() : "",
                    passagem.getPoltrona(),
                    passagem.getDatasaida(),
                    passagem.getHorasaida(),
                    passagem.getValorpassagem(),
                    totfaturamento = totfaturamento + passagem.getValorpassagem()
                });
            }

            lbtotfaturamento.setText(totfaturamento.toString());
            tlistacidade.setModel(model);

            // Rest of the code ...
        } catch (ParseException e) {
            // Handle the parsing exception appropriately, e.g., show an error message
            e.printStackTrace(); // You may want to log the exception as well
        } catch (SQLException e) {
            // Handle SQL exception
            e.printStackTrace(); // You may want to log the exception as well
        } catch (Exception ex) {
            // Handle other exceptions
            Logger.getLogger(Faturamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

