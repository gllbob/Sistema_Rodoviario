   private void popularComboCidadesOrigem() {
        try {
            CidadeDAO cidadedao = new CidadeDAO();
            ResultSet cidades = cidadedao.listarCidades();
            
            VeiculoDAO veiculodao = new VeiculoDAO();
            ResultSet veiculos = veiculodao.listarVeiculos();

            cbidecidadeorigem.removeAllItems();
            cbidecidadedestino.removeAllItems();
            cbdesplaca.removeAllItems();

            while (cidades.next()) {
                id.addElement(cidades.getInt(1));
                cbidecidadeorigem.addItem(cidades.getString(2));
            }

            cidades.close();
            cidades = cidadedao.listarCidades();

            while (cidades.next()) {
                id2.addElement(cidades.getInt(1));
                cbidecidadedestino.addItem(cidades.getString(2));
            }

            cidades.close();
            veiculos = veiculodao.listarVeiculos();

            while (veiculos.next()) {
                cbdesplaca.addItem(veiculos.getString(1));
            }

            cidades.close();

        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    
